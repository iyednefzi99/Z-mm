/** Substitue {nom} dans un gabarit. */
export const gabarit = (modele: string, valeurs: Record<string, string>): string =>
  modele.replace(/\{(\w+)\}/g, (_, cle: string) => valeurs[cle] ?? `{${cle}}`);
